public class ConstructorOverload
{
	//Constructor 1
	public ConstructorOverload()
	{
		System.out.println("Constructor 1 is called...");
	}
	//Constructor 2
	public ConstructorOverload(int p1)
	{
		System.out.println("Constructor 2 is called...");
	}
	//Constructor 3
	public ConstructorOverload(int p1, int p2)
	{
		System.out.println("Constructor 3 is called...");
	}
	//Constructor 4
	public ConstructorOverload(double p1)
	{
		System.out.println("Constructor 4 is called...");
	}
	//Constructor 5
	public ConstructorOverload(int p1, double p2)
	{
		System.out.println("Constructor 5 is called...");
	}
	//Constructor 6
	public ConstructorOverload(double p1 ,int p2)
	{
		System.out.println("Constructor 6 is called...");
	}
}